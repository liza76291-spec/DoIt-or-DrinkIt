function initSwipeListener(element) {
    if (!element) return;
    
    element.addEventListener('touchstart', (e) => {
        if (isAnimating) return;
        const touch = e.touches[0];
        startX = touch.clientX;
        startY = touch.clientY;
        currentCard = e.currentTarget;
    }, { passive: true });
    
    element.addEventListener('touchmove', (e) => {
        if (isAnimating || !currentCard) return;
        
        const touch = e.touches[0];
        const deltaX = touch.clientX - startX;
        const deltaY = touch.clientY - startY;
        
        if (Math.abs(deltaX) > 10 || Math.abs(deltaY) > 10) {
            e.preventDefault();
            currentCard.style.transform = `translate(${deltaX * 0.5}px, ${deltaY * 0.3}px) rotate(${deltaX * 0.03}deg)`;
        }
    }, { passive: false });
    
    element.addEventListener('touchend', (e) => {
        if (isAnimating || !currentCard) {
            currentCard = null;
            return;
        }
        
        const endX = e.changedTouches[0].clientX;
        const endY = e.changedTouches[0].clientY;
        const deltaX = endX - startX;
        const deltaY = endY - startY;
        
        currentCard.style.transform = '';
        
        if (Math.abs(deltaX) > 80) {
            isAnimating = true;
            const direction = deltaX > 0 ? 'right' : 'left';
            removeCurrentCard(direction);
        } else if (Math.abs(deltaY) > 80) {
            isAnimating = true;
            const direction = deltaY > 0 ? 'down' : 'up';
            removeCurrentCard(direction);
        }
        
        currentCard = null;
    });
    
    // Для мыши (десктоп)
    element.addEventListener('mousedown', (e) => {
        if (isAnimating) return;
        startX = e.clientX;
        startY = e.clientY;
        currentCard = e.currentTarget;
        currentCard.style.cursor = 'grabbing';
    });
    
    window.addEventListener('mousemove', (e) => {
        if (isAnimating || !currentCard) return;
        const deltaX = e.clientX - startX;
        const deltaY = e.clientY - startY;
        if (Math.abs(deltaX) > 10 || Math.abs(deltaY) > 10) {
            currentCard.style.transform = `translate(${deltaX * 0.5}px, ${deltaY * 0.3}px) rotate(${deltaX * 0.03}deg)`;
        }
    });
    
    window.addEventListener('mouseup', (e) => {
        if (isAnimating || !currentCard) {
            currentCard = null;
            return;
        }
        
        const deltaX = e.clientX - startX;
        const deltaY = e.clientY - startY;
        
        currentCard.style.transform = '';
        
        if (Math.abs(deltaX) > 80) {
            isAnimating = true;
            const direction = deltaX > 0 ? 'right' : 'left';
            removeCurrentCard(direction);
        } else if (Math.abs(deltaY) > 80) {
            isAnimating = true;
            const direction = deltaY > 0 ? 'down' : 'up';
            removeCurrentCard(direction);
        }
        
        currentCard = null;
        if (currentCardElement) {
            currentCardElement.style.cursor = 'grab';
        }
    });
}

